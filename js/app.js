function sortDataS(props, order) {
  menu.sort(function (a, b) {
    if (order == 'Desc') {
      if (a[props] > b[props]) {
        return -1;
      }
      else {
        return 1;
      }
    }
    else {
      if (a[props] > b[props]) {
        return 1;
      }
      else {
        return -1;
      }
    }
  })
  display(menu);

}

